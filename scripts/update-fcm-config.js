const fs = require('fs');
const path = require('path');

require('dotenv').config({ path: path.join(__dirname, '../.env.local') });

const swPath = path.join(__dirname, '../public/firebase-messaging-sw.js');
const packageJsonPath = path.join(__dirname, '../package.json');

function getFirebaseVersion() {
  const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, 'utf8'));
  const declaredVersion = packageJson.dependencies?.firebase ?? '12.10.0';
  const normalizedVersion = declaredVersion.replace(/^[^\d]*/, '');

  if (!normalizedVersion) {
    throw new Error('Unable to resolve the Firebase SDK version for the service worker.');
  }

  return normalizedVersion;
}

const config = {
  apiKey: process.env.NEXT_PUBLIC_FIREBASE_API_KEY,
  authDomain: process.env.NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN,
  projectId: process.env.NEXT_PUBLIC_FIREBASE_PROJECT_ID,
  storageBucket: process.env.NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET,
  messagingSenderId: process.env.NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID,
  appId: process.env.NEXT_PUBLIC_FIREBASE_APP_ID,
};

const missingEnvVars = Object.entries(config)
  .filter(([, value]) => !value)
  .map(([key]) => key);

if (missingEnvVars.length > 0) {
  if (!process.env.CI) {
    process.stderr.write('Missing Firebase environment variables for the messaging service worker:\n');
    missingEnvVars.forEach((key) => process.stderr.write(`- ${key}\n`));
    process.stderr.write('Skipping firebase-messaging-sw.js generation.\n');
  }
  process.exit(0);
}

const firebaseVersion = getFirebaseVersion();

const swContent = `// Auto-generated by scripts/update-fcm-config.js
// This worker is imported into the root /sw.js generated by next-pwa.
importScripts('https://www.gstatic.com/firebasejs/${firebaseVersion}/firebase-app-compat.js');
importScripts('https://www.gstatic.com/firebasejs/${firebaseVersion}/firebase-messaging-compat.js');

firebase.initializeApp({
  apiKey: '${config.apiKey}',
  authDomain: '${config.authDomain}',
  projectId: '${config.projectId}',
  storageBucket: '${config.storageBucket}',
  messagingSenderId: '${config.messagingSenderId}',
  appId: '${config.appId}'
});

const messaging = firebase.messaging();

function extractNotificationPayload(payload) {
  const notification = payload?.notification ?? {};
  const data = payload?.data ?? {};
  const webpushNotification = payload?.webpush?.notification ?? {};

  return {
    title: notification.title || data.title || webpushNotification.title || 'QuorumFlow',
    body: notification.body || data.body || webpushNotification.body || 'Tienes una nueva notificacion.',
    url: data.url || payload?.fcmOptions?.link || payload?.webpush?.fcmOptions?.link || '/',
    tag: data.tag || webpushNotification.tag || 'quorumflow-notification',
    icon: notification.icon || webpushNotification.icon || '/logo.svg',
    badge: notification.badge || webpushNotification.badge || '/logo.svg',
  };
}

messaging.onBackgroundMessage((payload) => {
  const message = extractNotificationPayload(payload);

  self.registration.showNotification(message.title, {
    body: message.body,
    icon: message.icon,
    badge: message.badge,
    tag: message.tag,
    renotify: true,
    data: {
      url: message.url,
    },
    vibrate: [200, 100, 200],
  });
});

self.addEventListener('notificationclick', (event) => {
  event.notification.close();

  const urlToOpen = event.notification?.data?.url || '/';

  event.waitUntil(
    clients.matchAll({
      type: 'window',
      includeUncontrolled: true,
    }).then((clientList) => {
      for (const client of clientList) {
        if ('focus' in client && client.url.includes(urlToOpen)) {
          return client.focus();
        }
      }

      if (clients.openWindow) {
        return clients.openWindow(urlToOpen);
      }

      return undefined;
    })
  );
});
`;

fs.writeFileSync(swPath, swContent, 'utf8');

process.stdout.write('Generated firebase-messaging-sw.js with synced Firebase SDK configuration.\n');
process.stdout.write(`Firebase SDK version: ${firebaseVersion}\n`);
process.stdout.write(`Project ID: ${config.projectId}\n`);
